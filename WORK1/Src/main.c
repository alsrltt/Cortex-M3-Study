int main(void)
{
  HAL_Init();
SystemClock_Config();
  MX_GPIO_Init();
  MX_USART1_UART_Init();
  while (1)
  {
    if(!(HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_0))){
      HAL_UART_Transmit(&huart1, "R" , 1 , 2);
    }
    if(!(HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_1))){
      HAL_UART_Transmit(&huart1, "G" , 1 , 2);
    }
    if(!(HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_2))){
      HAL_UART_Transmit(&huart1, "B" , 1 , 2);
    }

    HAL_Delay(200);
  }

}

